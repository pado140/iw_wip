    private void update(){
        ArrayList<Object> dat=new ArrayList<>();
        if(MODULE.getSelectedIndex()>0){
            String table=(TABLE.getSelectedIndex()>0?TABLE.getSelectedItem().toString():"Both");
            String update="update packing set Module=?,tablepacking=?";
            dat.add(MODULE.getSelectedItem().toString());
            dat.add(table);
            if(blockDate.isEnabled()&&jDateChooser1.getDate()!=null){
                update+=",created=?";
                dat.add(jDateChooser1.getDate());
            }
            dat.add(pack_id);
            update +=" where id=?";
            if(conn.Update(update, 0, dat.toArray())){
                JOptionPane.showMessageDialog(this, "LPN update successfully ", "success", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }
    
    private void delete(){
        
            String update="delete from  packing ";
            
            update +=" where id=?";
            if(conn.Update(update, 0, pack_id)){
                JOptionPane.showMessageDialog(this, "LPN unscan successfully ", "success", JOptionPane.INFORMATION_MESSAGE);
            }
        
    }
    private Map<String,String> getBoxPackingInfos(String box){
        String requete="select * from packed_lpn where lpnScan=? or box_stickers=?";
        ResultSet rs=conn.select(requete, box,box);
        Map<String,String> boxinfos=new HashMap<>();
        try {
            while (rs.next()){
                boxinfos.put("PO", rs.getString("PO"));
                boxinfos.put("ID", String.valueOf(rs.getInt("Pack_id")));
                boxinfos.put("SKU", rs.getString("sku"));
                boxinfos.put("QTY", String.valueOf(rs.getInt("QTY")));
                boxinfos.put("STICKER", rs.getString("BOX_STICKERS"));
                boxinfos.put("TABLE", rs.getString("tablepacking"));
                boxinfos.put("MODULE", rs.getString("module"));
                boxinfos.put("DATE", rs.getString("scan_date"));
                boxinfos.put("STATUS", rs.getString("status"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(UpdatePackingInfos.class.getName()).log(Level.SEVERE, null, ex);
        }
        return boxinfos;
    }

