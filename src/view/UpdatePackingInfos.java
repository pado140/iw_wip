    private void SCANKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_SCANKeyReleased
        // TODO add your handling code here:
        if(evt.getKeyCode()==KeyEvent.VK_ENTER){
            String code=((JTextField)evt.getSource()).getText().trim();
            if(code.isEmpty()){
                JOptionPane.showInternalMessageDialog(this, "scan the box sticker first");
                return;
            }
            Map<String,String> data=getBoxPackingInfos(code);
            if(data.isEmpty()){
                JOptionPane.showInternalMessageDialog(this, "No imformation for this box\n");
                init();
                return;
            }
            POLABEL.setText(data.get("PO"));
            SKULABEL.setText(data.get("SKU"));
            QTYLABEL.setText(data.get("QTY"));
            SCAN.setText("");
            MODULE.setSelectedItem(data.get("MODULE"));
            TABLE.setSelectedItem(data.get("TABLE"));
            STICKERLABEL.setText(data.get("STICKER"));
            pack_id=Integer.parseInt(data.get("ID"));
            jButton2.setEnabled(true);
            if(data.get("STATUS").trim().equalsIgnoreCase("0")){
                blockDate.setEnabled(true);
                jDateChooser1.setEnabled(true);
                jButton1.setEnabled(true);
            }
        }
    }//GEN-LAST:event_SCANKeyReleased

