private void fillData(){
        String requete="select ponum,style,sku,coldsp,size,lpn,box_stickers, ordnum,qty,status_lpn from lpn_in_batch where box_stickers = ?";
        ResultSet rs=conn.select(requete, sticker.getText().trim());
        String status="";
        error.setText("");
        oldqtylabel.setText("N/A");
        polabel.setText("N/A");
        stylelabel.setText("N/A");
        colorlabel.setText("N/A");
        sizelabel.setText("N/A");
        try {
            while(rs.next())
            {
                sticker_selected=sticker.getText().trim();
                oldqtylabel.setText(String.valueOf(rs.getInt("qty")));
                polabel.setText(rs.getString("ponum"));
                stylelabel.setText(rs.getString("style"));
                colorlabel.setText(rs.getString("coldsp"));
                sizelabel.setText(rs.getString("size"));
                ordnum=rs.getString("ordnum").trim();
                status=rs.getString("status_lpn");
            }
        } catch (SQLException ex) {
            Logger.getLogger(lpn_update.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(sticker_selected.isEmpty()){
            error.setText("Sticker# :"+sticker.getText().trim()+" is not valid");
        }else if(status.contains("pass")){
            error.setText("Sticker# :"+sticker.getText().trim()+" is already approved by the Outgoing Auditor"
                    + "");
        }
        
            error.setVisible(!error.getText().isEmpty());
    }
    
    private boolean updateVal(){
        if(Integer.parseInt(oldqtylabel.getText())>(Integer)new_qty.getValue()){
            String requete="update batches_lpn set qty=?, old_qty=?,modified=getDate() where box_stickers=?";
            boolean check= conn.Update(requete, 0, new Object[]{new_qty.getValue(),oldqtylabel.getText(),sticker_selected});
            String requete_1="insert into TRANSAC(TRANSACT,ITEM,QTY,ACT_TYPE,ACT_NAME,SUB_ITEM,QTY_SUBITEM,user_id) values ('post sewing',?,?,5,?,?,?,?)";
                        conn.Update(requete_1, 0, new Object[]{ordnum,oldqtylabel.getText(),"box change",sticker_selected,new_qty.getValue(),Principal.user_id});
                        return true;
        }
        JOptionPane.showMessageDialog(this, "the new quantite must be less then the original qty", "Warning", JOptionPane.WARNING_MESSAGE);
        return false;
    }

